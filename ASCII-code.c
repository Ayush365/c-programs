//print ASCII number
/* its a simple code , we just need to input a variable as character
then print it as integer , this will print the ascii number/code of that character,
for example - try to write/input 'a' in terminal and it will print 97 , which is the ascii code of 'a' */
#include<stdio.h>
int main()
{
    char ch;
    scanf("%c",&ch);
    printf("%d",ch);
    return 0;
}
